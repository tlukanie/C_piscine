int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb < 0)
		return (0);
	while (i < 46341)
	{
		if (nb == (i * i))
			return (i);
		if (nb < (i * i))
			return (0);
		i++;
	}
	return (0);
}
