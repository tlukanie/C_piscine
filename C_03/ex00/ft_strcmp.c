int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	j;
	int	result;

	i = 0;
	j = 0;
	result = 0;
	while (s1[i] != '\0' && s1[i] == s2[j])
	{
		++i;
		++j;
	}
	result = s1[i] - s2[i];
	return (result);
}
